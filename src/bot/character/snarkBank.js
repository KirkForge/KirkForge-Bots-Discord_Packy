/**
 * Get N random non-repeating snark lines from a specific category
 * @param {string} category - Category key (base, lore, chromebook, tech_humor, code_comments)
 * @param {number} n - Number of lines to return (default 3)
 * @returns {string[]} Array of random snark lines
 */
export function getSnarkLines(category = 'base', n = 3) {
  const lines = snarkBank[category] || snarkBank.base;
  if (n <= 0) return [];
  if (n >= lines.length) return fisherYatesShuffle([...lines]);

  // Fisher-Yates shuffle to get non-repeating random items
  const shuffled = fisherYatesShuffle([...lines]);
  return shuffled.slice(0, Math.min(n, shuffled.length));
}

/**
 * Get one random snark line from the entire pool, optionally excluding categories
 * @param {string[]} excludeCategories - Categories to exclude (default [])
 * @returns {string} A random snark line
 */
export function getRandomSnark(excludeCategories = []) {
  const categories = Object.keys(snarkBank).filter(cat => !excludeCategories.includes(cat));

  if (categories.length === 0) {
    return "I've lost all my snark. Please restore my dignity.";
  }

  // Pick a random category
  const category = categories[Math.floor(Math.random() * categories.length)];
  const lines = snarkBank[category];

  // Return a random line from that category using proper random indexing
  return lines[Math.floor(Math.random() * lines.length)];
}

/**
 * Get N lines drawn from random categories
 * @param {number} n - Number of lines to return (default 3)
 * @returns {string[]} Array of random snark lines from mixed categories
 */
export function getMixedSnark(n = 3) {
  const allLines = Object.values(snarkBank).flat();

  if (n <= 0) return [];
  if (n >= allLines.length) return fisherYatesShuffle([...allLines]);

  // Fisher-Yates shuffle to select n items without replacement
  const shuffled = fisherYatesShuffle([...allLines]);
  return shuffled.slice(0, Math.min(n, shuffled.length));
}

/**
 * Fisher-Yates shuffle implementation for unbiased random selection
 * @param {Array} arr - Array to shuffle in-place
 * @returns {Array} The shuffled array
 */
function fisherYatesShuffle(arr) {
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}
